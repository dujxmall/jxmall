namespace app\forms\common\file;


use app\core\ApiCode;
use app\models\BaseModel;
use app\models\FileGroup;

class FileGroupDeleteForm extends BaseModel
{


    public $id;

    public function rules()
    {
        return [[['id'], 'required'],
            [['id'], 'integer']

        ]; // TODO: Change the autogenerated stub
    }

    public function delete()
    {
        if (!$this->validate()) {
            return $this->responseErrorInfo();
        }
        $group = FileGroup::findOne(['id' => $this->id, 'is_delete' => 0]);
        if (!$group) {
            return $this->apiResponse(ApiCode::CODE_FAIL, '文件组不存在或已被删除');
        }
        $group->is_delete = 1;
        if (!$group->save()) {
            return $this->responseErrorMsg($group);
        }
        return $this->apiResponse(ApiCode::CODE_SUCCESS, '删除成功');
    }
}