namespace app\forms\mall\auth;


use app\core\ApiCode;
use app\helpers\ResponseHelper;
use app\models\AuthItem;
use app\models\BaseModel;
use yii\rbac\Assignment;

class AssignForm extends BaseModel
{
    public $role_name;
    public $admin_id;

    public function rules()
    {
        return [
            [['role_name'], 'string'],
            [['admin_id'], 'integer'],
        ]; // TODO: Change the autogenerated stub
    }

    public function save()
    {
        if (!$this->validate()) {
            return $this->responseErrorInfo();
        }
        $authManager = \Yii::$app->authManager;
        if (!$authManager->getRole($this->role_name)) {
            return ResponseHelper::send(ApiCode::CODE_FAIL, '角色不存在！');
        }
        if (!$authManager->assign($this->role_name, $this->admin_id)) {
            return ResponseHelper::send(ApiCode::CODE_FAIL, '保存失败！');
        }
        return ResponseHelper::send(ApiCode::CODE_SUCCESS, '保存成功！');
    }

}
