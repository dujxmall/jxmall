namespace app\forms\mall\order;


use app\core\ApiCode;
use app\models\BaseModel;
use app\models\Eorder;
use app\models\ExpressLog;

class ExpressLogListForm extends BaseModel
{
    public $page;
    public $limit;
    public $status;
    public $keyword_type;
    public $keyword;
    public $search_date;


    public function rules()
    {
        return [
            [['keyword_type', 'keyword'], 'string'],
            [['page', 'limit', 'status'], 'integer'],
            [['limit'], 'default', 'value' => 10],
            [['search_date'], 'safe']
        ]; // TODO: Change the autogenerated stub
    }

    public function getList()
    {

        if (!$this->validate()) {
            return $this->responseErrorInfo();
        }

        $query = ExpressLog::find()->where(['is_delete' => 0, 'mall_id' => \Yii::$app->mall->id])
            ->orderBy('updated_at desc');

        if($this->keyword){
            $query->andWhere(['or',['like','name',$this->keyword],['like','address',$this->keyword],['like','express_no',$this->keyword],['like','mobile',$this->keyword]]);
        }
        $list = $query->page($pagination, $this->limit, $this->page)
            ->asArray()->all();

        foreach ($list as &$item) {
            $item['updated_at'] = date('Y-m-d H:i:s', $item['updated_at']);
            if ($item['eorder_id']) {
                $eorder = Eorder::findOne(['id' => $item['eorder_id'], 'is_delete' => 0]);
                if ($eorder) {

                    $item['eorder_name'] = $eorder->name;
                }
            }
        }
        unset($item);
        return $this->apiResponse(ApiCode::CODE_SUCCESS, '', ['list' => $list,'pagination'=>$pagination]);
    }
}