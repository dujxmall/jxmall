namespace app\forms\mall\order;


use app\core\ApiCode;
use app\models\BaseModel;
use app\models\Order;

class OrderAddressModifyForm extends BaseModel
{
    public $province_code;
    public $city_code;
    public $county_code;

    public $town_code;
    public $address;
    public $order_id;


    public function rules()
    {
        return [
            [['province_code', 'city_code', 'county_code', 'order_id'], 'required'],
            [['province_code', 'city_code', 'county_code', 'town_code', 'order_id'], 'integer'],
            [['address'], 'string'],
            [['town_code'], 'default', 'value' => 0]
        ];// TODO: Change the autogenerated stub
    }

    public function save()
    {
        if (!$this->validate()) {
            return $this->responseErrorInfo();
        }
        $order = Order::findOne(['is_delete' => 0, 'id' => $this->order_id]);
        if (!$order) {
            return $this->apiResponse(ApiCode::CODE_FAIL, '订单不存在，或已被删除');
        }
        $order->town_code = $this->town_code;
        $order->county_code = $this->county_code;
        $order->city_code = $this->city_code;
        $order->province_code = $this->province_code;
        $order->address = $this->address;
        if (!$order->save()) {
            return $this->responseErrorMsg($order);
        }
        return $this->apiResponse(ApiCode::CODE_SUCCESS, '保存成功');
    }


}