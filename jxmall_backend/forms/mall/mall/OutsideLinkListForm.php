namespace app\forms\mall\mall;


use app\core\ApiCode;

use app\models\BaseModel;
use app\models\OutsideLink;

class OutsideLinkListForm extends BaseModel
{
    public $name;
    public $page;
    public $link;
    public function rules()
    {
        return [
            [['page'], 'integer'],
            [['name', 'link'], 'string']
        ]; // TODO: Change the autogenerated stub
    }

    public function getList()
    {
        if (!$this->validate()) {

            return $this->responseErrorInfo();
        }
        $query = OutsideLink::find()->where(['mall_id' => \Yii::$app->mall->id, 'is_delete' => 0]);
        $query->andFilterWhere(['like', 'name', $this->name]);
        $query->andFilterWhere(['like', 'link', $this->link]);
        $list = $query->page($pagination, $this->limit, $this->page)->orderBy('created_at DESC')->asArray()->all();
        return $this->apiResponse(ApiCode::CODE_SUCCESS, '', ['list' => $list, 'pagination' => $pagination]);
    }
}