namespace app\forms\api\order;


use app\core\ApiCode;
use app\helpers\DateHelper;
use app\helpers\ResponseHelper;
use app\models\BaseModel;
use app\models\Order;

class ApplySendForm extends BaseModel
{
    public $order_id;

    public function rules()
    {
        return [
            [['order_id'], 'integer'],
        ]; // TODO: Change the autogenerated stub
    }

    public function send()
    {
        if (!$this->validate()) {
            return $this->responseErrorInfo();
        }
        $order = Order::getOne($this->order_id);
        if (!$order) {
            return ResponseHelper::send(ApiCode::CODE_FAIL, '订单不存在或被删除');
        }
        $order->is_apply_send = 1;

        $order->apply_send_time = DateHelper::format(time());

        if (!$order->save()) {
            return $this->responseErrorMsg($order);
        }
        return ResponseHelper::send(ApiCode::CODE_SUCCESS,'申请成功,将尽快为您发货!');

    }


}