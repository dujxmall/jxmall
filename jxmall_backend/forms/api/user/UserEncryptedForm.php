namespace app\forms\api\user;


use app\core\ApiCode;
use app\helpers\ResponseHelper;
use app\helpers\WechatHelper;
use app\models\BaseModel;

class UserEncryptedForm extends BaseModel
{
    public $code;

    public $encryptedData;

    public $iv;

    public function rules()
    {
        return [
            [['code', 'iv', 'encryptedData'], 'string']
        ]; // TODO: Change the autogenerated stub
    }

    public function encrypted()
    {
        if (!$this->validate()) {
            return $this->responseErrorInfo();
        }
        try {
            WechatHelper::init(\Yii::$app->mall->id);
            $app = \Yii::$app->wechat->miniProgram;
            $session = $app->auth->session($this->code);
            $decryptedData = $app->encryptor->decryptData($session['session_key'], $this->iv, $this->encryptedData);
            return ResponseHelper::send(ApiCode::CODE_SUCCESS, '', ['data' => $decryptedData]);
        } catch (\Exception $exception) {
            return ResponseHelper::send(ApiCode::CODE_FAIL, '消息解密失败，请重试');
        }

    }

}