namespace app\forms\admin;


use app\core\ApiCode;
use app\helpers\ResponseHelper;
use app\models\Api;
use app\models\BaseModel;

class ApiListForm extends BaseModel
{

    public $page;
    public $path;

    public $description;

    public $api_group;
    public $limit = 10;

    public $method;

    public function rules()
    {
        return [
            [['path', 'description', 'api_group', 'method'], 'string'],
            [['page', 'limit'], 'integer']
        ]; // TODO: Change the autogenerated stub
    }


    public function getList()
    {

        $query = Api::find()->andWhere(['is_delete' => 0])->orderBy('id desc');
        $query->andFilterWhere(['like', 'path', $this->path]);
        $query->andFilterWhere(['like', 'description', $this->description]);
        $query->andFilterWhere(['like', 'api_group', $this->api_group]);
        $query->andFilterWhere(['like', 'method', $this->method]);
        $list = $query->page($pagination, $this->limit, $this->page)->asArray()->all();
        return ResponseHelper::send(ApiCode::CODE_SUCCESS, '', ['list' => $list, 'pagination' => $pagination]);
    }

}