namespace app\plugins\share\forms\mall;


use app\core\ApiCode;
use app\helpers\OptionHelper;
use app\models\BaseModel;
use app\plugins\share\models\ShareLevel;

class SettingForm extends BaseModel
{
    public $level;
    public $self_buy;
    public $type;
    public $price;
    public $list;
    public $explain;
    public $re_apply;


    public function rules()
    {
        return [
            [['explain', 'price'], 'string'],
            [['list'], 'safe'],
            [['level', 'self_buy', 'type','re_apply'], 'integer'],
        ]; // TODO: Change the autogenerated stub
    }

    public function save()
    {
        if (!$this->validate()) {
            return $this->responseErrorInfo();
        }
        OptionHelper::set('share_setting', $this->attributes, \Yii::$app->mall->id);
        if ($this->level > 0) {
            $level = ShareLevel::findOne(['mall_id' => \Yii::$app->mall->id, 'level' => 0]);
            if (!$level) {
                $level = new ShareLevel();
                $level->name = "默认等级";
                $level->level = 0;
                $level->mall_id = \Yii::$app->mall->id;
                $level->first_price = 0;
                $level->second_price = 0;
                $level->third_price = 0;
                $level->save();
            }
        }

        return $this->apiResponse(ApiCode::CODE_SUCCESS, '保存成功');
    }

    public function search()
    {
        $setting = OptionHelper::get('share_setting', \Yii::$app->mall->id);
        if (!$setting) {
            return $this->apiResponse(ApiCode::CODE_FAIL, '暂未配置');
        }
        return $this->apiResponse(ApiCode::CODE_SUCCESS, '', ['setting' => $setting]);
    }
}