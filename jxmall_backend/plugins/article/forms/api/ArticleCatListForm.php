namespace app\plugins\article\forms\api;


use app\core\ApiCode;
use app\helpers\DateHelper;
use app\helpers\ResponseHelper;
use app\plugins\article\models\ArticleCat;

class ArticleCatListForm
{

    public $limit = 10;
    public $page;


    public function rules()
    {
        return [
            [['limit', 'page'], 'integer'],

        ]; // TODO: Change the autogenerated stub
    }


    public function getList()
    {
        $query = ArticleCat::find()->where(['mall_id' => \Yii::$app->mall->id, 'is_delete' => 0]);
        if ($this->page) {
            $query->page($pagination, $this->limit, $this->page);
        }
        $list = $query->asArray()->all();
        foreach ($list as &$item) {
            $item['created_at'] = DateHelper::format($item['created_at']);
        }
        unset($item);
        return ResponseHelper::send(ApiCode::CODE_SUCCESS, '', ['list' => $list, 'pagination' => $pagination]);
    }
}